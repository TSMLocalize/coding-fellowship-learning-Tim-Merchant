// Ternary Operator

// Called ternary operator because it consists of 3 parts

let y = (x > 10) ? 13 : 3335;

// Statement - code that does something
// let z = 
// ^ this a statement, do some code that tells JS to do something.

// Expression - code that equals a value
// If it can be stored in a variable, then it must be an expression.

switch (x) {
    case 5:
        console.log('X is 5#');
        break;
    case 6:
        console.log('X is 6#');
        break;
    default:
        console.log('X is #');
}

// Only useful if you're checking the same value